export function PlatformLogo({ id, className = "", title, decorative = false }: PlatformLogoProps) {
  const label = title ?? LOGO_LABELS[id];

  return (
    <svg
      className={`${styles.logo} ${className}`.trim()}
      viewBox="0 0 24 24"
      xmlns="http://www.w3.org/2000/svg"
      role={decorative ? "presentation" : "img"}
      aria-label={decorative ? undefined : label}
      aria-hidden={decorative ? true : undefined}
    >
      {decorative ? null : <title>{label}</title>}
      <LogoPath id={id} />
    </svg>
  );
}

type PlatformLogoGroupProps = {
  ids: readonly PlatformLogoId[];
  className?: string;
  size?: "small" | "medium" | "large";
};

export function PlatformLogoGroup({ ids, className = "", size = "medium" }: PlatformLogoGroupProps) {
  if (ids.length === 0) return null;

  return (
    <div
      className={`${styles.group} ${styles[size]} ${className}`.trim()}
      role="img"
      aria-label={ids.map((id) => LOGO_LABELS[id]).join(", ")}
    >
      {ids.map((id) => (
        <PlatformLogo key={id} id={id} decorative />
      ))}
    </div>
  );
}

type PlatformLogoPanelProps = {
  ids: readonly PlatformLogoId[];
  className?: string;
};

export function PlatformLogoPanel({ ids, className = "" }: PlatformLogoPanelProps) {
  if (ids.length === 0) return null;

  return (
    <div className={`${styles.panel} ${className}`.trim()}>
      <PlatformLogoGroup ids={ids} size="large" />
    </div>
  );
}
